#include <stdio.h>

// For function 1: To add two integers
int add(int a, int b) {
    return a + b;
}

// For Function 2: To multiply two integers
int multiply(int a, int b) {
    return a * b;
}

// For Function 3:  To subtract two integers
int subtract(int a, int b) {
    return a - b;
}
